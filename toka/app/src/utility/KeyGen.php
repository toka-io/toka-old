<?php
class KeyGen {

    public static function getRandomKey($numOfBytes) {
        return bin2hex(openssl_random_pseudo_bytes($numOfBytes));
    }
    
    public static function getRandomStringKey($length) {
        $characters = "0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ_-";
        $charactersLength = strlen($characters);
        $randomString = "";
        for ($i = 0; $i < $length; $i++) {
            $randomString .= $characters[rand(0, $charactersLength - 1)];
        }
        return $randomString;
    }
}